const byId = new Map(MOCK_PRODUCTS.map((p) => [p.id, p]));

export function getProducts(): Product[] {
  return [...MOCK_PRODUCTS].sort(
    (a, b) => new Date(b.created_at).getTime() - new Date(a.created_at).getTime()
  );
}

export function getProductById(id: string): Product | undefined {
  return byId.get(id);
}

export function getRelatedProducts(category: string, excludeId: string, limit = 4): Product[] {
  return MOCK_PRODUCTS.filter((p) => p.category === category && p.id !== excludeId).slice(0, limit);
}

